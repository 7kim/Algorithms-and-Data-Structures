//sample class for making linked list
public class sample
{
    private int a;
    private int b;

    //setters
    public void setA(int a)
    {
        this.a = a;
    }
    public void setB(int b)
    {
        this.b = b;
    } 
    //getters
    public int getA()
    {
        return a;
    }
    public int getB()
    {
        return b;
    }
    public String toString()
    {
        return a+ " "+b;
    }
}